impl Catalog {
    pub fn connection_state(&self, state: ConnectionState) -> &'static str {
        match state {
            ConnectionState::Disconnected => self.disconnected,
            ConnectionState::Connecting => self.connecting,
            ConnectionState::Connected => self.connected,
            ConnectionState::Disconnecting => self.disconnecting,
            ConnectionState::Error => self.attention_required,
        }
    }

    pub fn credential_error(&self, error: CredentialError) -> &'static str {
        match error {
            CredentialError::EmptyServer => self.empty_server,
            CredentialError::InvalidServer => self.invalid_server,
            CredentialError::EmptyUsername => self.empty_username,
            CredentialError::EmptyPassword => self.empty_password,
        }
    }

    pub fn backend_error(&self, error: &VpnBackendError) -> String {
        let Some(message) = error.localized_message() else {
            return error.to_string();
        };

        let translated = match message {
            BackendErrorMessage::UnsupportedPlatform => self.unsupported_platform,
            BackendErrorMessage::SystemAuthorizationUnavailable => {
                self.system_authorization_unavailable
            }
            BackendErrorMessage::OpenConnectNotFound => self.openconnect_not_found,
            BackendErrorMessage::PkexecNotFound => self.pkexec_not_found,
            BackendErrorMessage::AuthorizationCancelled => self.authorization_cancelled,
            BackendErrorMessage::ConnectionUnavailable => self.connection_unavailable,
            BackendErrorMessage::PolkitStartFailed => self.polkit_start_failed,
            BackendErrorMessage::DisconnectFailed => self.disconnect_failed,
            BackendErrorMessage::DisconnectStillActive => self.disconnect_still_active,
            BackendErrorMessage::ProcessMonitoringFailed => self.process_monitoring_failed,
            BackendErrorMessage::StateStoreFailed => self.state_store_failed,
            BackendErrorMessage::ConnectionStartFailed => self.connection_start_failed,
        };

        match error.detail() {
            Some(detail) if !detail.trim().is_empty() => format!("{translated}: {detail}"),
            _ => translated.to_owned(),
        }
    }
}

